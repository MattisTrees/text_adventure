def zombieMovement(hours):
    """This function will take hours and give a random zombie speed per hour that passes"""

    zombie_movement = 0

    while hours > 0:

        zombie_spd = random.randrange(4, 11)
        zombie_movement += zombie_spd
        hours -= 1

    return zombie_movement

